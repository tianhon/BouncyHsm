using BouncyHsm.Core.Services.Contracts.P11;
using Org.BouncyCastle.Crypto;
using Org.BouncyCastle.Crypto.Parameters;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace BouncyHsm.Core.Services.Contracts.Entities;

public class MlDsaPublicKeyObject : PublicKeyObject
{
    public CKP CkaParameterSet
    {
        get => (CKP)this.values[CKA.CKA_PARAMETER_SET].AsUint();
        set => this.values[CKA.CKA_PARAMETER_SET] = AttributeValue.Create((uint)value);
    }

    public byte[] CkaValue
    {
        get => this.values[CKA.CKA_VALUE].AsByteArray();
        set => this.values[CKA.CKA_VALUE] = AttributeValue.Create(value);
    }

    internal MlDsaPublicKeyObject(StorageObjectMemento memento)
        : base(memento)
    {
    }

    public MlDsaPublicKeyObject() : base(CKK.CKK_ML_DSA, CKM.CKM_ML_DSA_KEY_PAIR_GEN)
    {
        this.CkaParameterSet = CKP.CKP_ML_DSA_44;
        this.CkaValue = Array.Empty<byte>();
    }

    public override void Accept(ICryptoApiObjectVisitor visitor)
    {
        visitor.Visit(this);
    }

    public override T Accept<T>(ICryptoApiObjectVisitor<T> visitor)
    {
        return visitor.Visit(this);
    }

    public override AsymmetricKeyParameter GetPublicKey()
    {
        return MLDsaPublicKeyParameters.FromEncoding(MlDsaUtils.GetParametersFromType(this.CkaParameterSet), this.CkaValue);
    }

    public override void SetPublicKey(AsymmetricKeyParameter publicKey)
    {
        if (publicKey is MLDsaPublicKeyParameters mLDsaPublicKey)
        {
            this.CkaParameterSet = MlDsaUtils.GetMlDsaparametersType(mLDsaPublicKey.Parameters);
            this.CkaValue = mLDsaPublicKey.GetEncoded();
        }
        else
        {
            throw new ArgumentException("publicKey is not MLDsaPublicKeyParameters", nameof(publicKey));
        }
    }

    public override void Validate()
    {
        base.Validate();
        CryptoObjectValueChecker.CheckEnumIsDefined<CKP>(CKA.CKA_PARAMETER_SET, this.CkaParameterSet);
        CryptoObjectValueChecker.CheckNotEmpty(CKA.CKA_VALUE, this.CkaValue);
    }
}
